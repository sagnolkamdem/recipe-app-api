def add(x, y):
    """
    The add function adds two numbers together and returns the result.

    param x: Pass the first value to be added
    param y: Provide a default value for the y parameter
    return: The sum of x and y
    doc-author: Trelent
    """
    return x + y


def substract(x, y):
    """
    The substract function subtracts two numbers.
    param x: Pass the first value to be substracted
    param y: Substract the y value from x
    return: The difference of x and y
    doc-author: Trelent
    """
    return x - y
